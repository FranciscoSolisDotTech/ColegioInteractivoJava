package xyz.theprogramsrc.colegiointeractivojava.superjcore.utils;

public class Cookie {

    private String name, value;

    public Cookie(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
